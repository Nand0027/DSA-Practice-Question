int main() {
    int n;
    cout<<"Enter a number : ";
    cin>>n;

    int i = 1;
    while (i<=n){
        int start = 1;
        cout<<start;
        start++;
    }
    int j=1;
    while(j<=n-i){
        cout<<i;
        j++;
    }
    cout<<endl;
    i++;
}